PropertyNotification CfgViewModel::get_Notification()
{
	return [this](uint32_t uID)
			{
				if( uID == PROPID_BACK_COLOR ) {
					const RgbColor& clr = this->m_refModel->GetBackColor();
					this->m_clrBack = fl_rgb_color(clr.r(), clr.g(), clr.b());
					this->Fire(uID);
				}
			};
}

