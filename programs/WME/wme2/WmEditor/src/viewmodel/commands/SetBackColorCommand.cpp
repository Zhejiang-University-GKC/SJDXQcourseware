std::function<bool(Fl_Color)> CfgViewModel::get_SetBackColorCommand()
{
	return [this](Fl_Color clr)->bool
			{
				uchar r, g, b;
				Fl::get_color(clr, r, g, b);
				this->m_refModel->SetBackColor(RgbColor(r, g, b));
				return true;
			};
}

